FULL_MARKER = "<!-- full-analysis-section -->"


def md(source: str):
    return nbf.v4.new_markdown_cell(source.strip())


def code(source: str):
    return nbf.v4.new_code_cell(source.strip())


def remove_existing_full_section(cells):
    cleaned = []
    skip = False
    for cell in cells:
        if cell.cell_type == "markdown" and FULL_MARKER in cell.source:
            skip = True
            continue
        if skip and cell.cell_type == "markdown" and (
            "Interpretation and Next Steps" in cell.source
            or "## 16. Workplan" in cell.source
            or "## 17. Workplan" in cell.source
        ):
            skip = False
            cleaned.append(cell)
            continue
        if skip:
            continue
        cleaned.append(cell)
    return cleaned


def renumber_sections(cell):
    if cell.cell_type != "markdown":
        return cell
    replacements = {
        "## 15. Interpretation and Next Steps": "## 16. Interpretation and Next Steps",
        "## 16. Workplan": "## 17. Workplan",
        "## 17. Proposal Summary": "## 18. Proposal Summary",
        "## 18. References and Library Ledger": "## 19. References and Library Ledger",
        "## 19. Export Outputs": "## 20. Export Outputs",
    }
    for old, new in replacements.items():
        cell.source = cell.source.replace(old, new)
    return cell


