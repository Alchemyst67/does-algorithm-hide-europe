def patch_outputs_manifest(cell):
    if cell.cell_type != "code" or "outputs_manifest = [" not in cell.source:
        return cell
    cell.source = cell.source.replace(
        'outputs_manifest = [\n    "00_research_gap_matrix.png",',
        'outputs_manifest = [\n    "00_research_gap_matrix.png",',
    )
    additions = [
        '    "11_language_country_company_caveats.png",',
        '    "11_metadata_proxy_risk_table.csv",',
        '    "12_full_model_comparison.csv",',
        '    "12_full_rerank_tradeoff.csv",',
        '    "12_full_run_report.csv",',
        '    "12_full_group_exposure_by_model.png",',
        '    "13_full_accuracy_fairness_summary.png",',
        '    "14_full_utility_vs_prominence_frontier.png",',
        '    "source_citation_ledger.csv",',
        '    "does_algorithm_hide_europe_proposal_deck.pptx",',
    ]
    if '"11_language_country_company_caveats.png"' not in cell.source:
        cell.source = cell.source.replace(
            '    "10_workplan_gantt.png",\n',
            '    "10_workplan_gantt.png",\n' + "\n".join(additions) + "\n",
        )
    cell.source = cell.source.replace(
        "These outputs are from a proposal-stage real-data run. No synthetic data was used. Wikidata coverage depends on the local cache and the configured proposal query limit. Do not treat the bounded run as final empirical evidence until the full run is executed.",
        "These outputs use real M3L/MovieLens/Wikidata data only. The notebook includes a proposal smoke test and then restores the larger full-run outputs generated by `scripts/run_full_cultural_prominence_audit.py`. The recommender full run uses 5,000 real users and an expanded candidate universe for local-compute feasibility.",
    )
    return cell


def main():
    nb = nbf.read(NOTEBOOK_PATH, as_version=4)
    cells = remove_existing_full_section(nb.cells)

    new_cells = []
    inserted = False
    for cell in cells:
        cell = renumber_sections(cell)
        cell = patch_outputs_manifest(cell)
        new_cells.append(cell)
        if cell.cell_type == "markdown" and cell.source.lstrip().startswith("## 14. Result Dashboard") and not inserted:
            # Insert after the full result-dashboard block, just before interpretation/next steps.
            pass

    final_cells = []
    for idx, cell in enumerate(new_cells):
        if (
            not inserted
            and cell.cell_type == "markdown"
            and "Interpretation and Next Steps" in cell.source
        ):
            final_cells.extend(FULL_SECTION_CELLS)
            inserted = True
        final_cells.append(cell)

    nb.cells = final_cells
    nbf.write(nb, NOTEBOOK_PATH)
    print(f"Added full-analysis section to {NOTEBOOK_PATH}")
    print(f"Cells: {len(nb.cells)}")


if __name__ == "__main__":
    main()
